static void set_color(uint8_t index, color_t * const color)
{
    color_rgb_t rgb;
    color_convert2(color->color_space, COLOR_SPACE_RGB, (color_kind_t *)color, (color_kind_t *)&rgb);

    npdata_set_color(index, &rgb);
}

